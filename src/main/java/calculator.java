public class calculator {
    //print parameter passed to the screen
    int addNumbers(int number1, int number2)
    {
        int result = number1 + number2;
        return result;

    }

    int SubstractNumbers(int number1, int number2)
    {
        int result = number1 - number2;
        return result;

    }

    int MultiplyNumbers(int number1, int number2)
    {
        int result = number1 * number2;
        return result;

    }

    int DiviseNumbers(int number1, int number2)
    {
        int result = number1 / number2;
        return result;

    }


}

